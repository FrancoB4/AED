# Datos: 3 temperaturas (float)
t1 = float(input("Temperatura 1 :"))
t2 = float(input("Temperatura 2 :"))
t3 = float(input("Temperatura 3 :"))

# Proceso: Promedio de temperatura (float)
promedio = (t1 + t2 + t3) / 3

# Salida: Promedio
print(f"El promedio de temperatura en los tres periodos fue: {promedio}")
